//                  Main Function
//----------------------------------------------------------------------------------------
int main(void){
	WDTCTL = WDTPW | WDTHOLD;	// stop watchdog timer
	

    // Configure and calibrate system clock
    //--------------------------------------
    DCOCTL = 0;                                  // Select lowest DCOx and MODx settings
    BCSCTL1 = CALBC1_1MHZ;                       // Set range
    DCOCTL = CALDCO_1MHZ;                        // Set DCO step + modulation */
    BCSCTL2 = 0x00;



    // Configure ports
    //------------------
    P1DIR = 0x35;                               // P1.0 has LED on this pin (MSP430 Launchpad)
    P1OUT = 0x08;                               // Bit 3 = 1 i.e. PULL UP

    P2DIR = 0x03;                               // Lower 4 bits are outputs
    P2OUT = 0x00;                               // Enable pull on P2.4, P2.5, P2.6 and P2.7

    // Configure WDT as systick timer
    //--------------------------------

    WDTCTL = WDT_MDLY_0_5 ;                     // Enable WDT with period of 500uS
    IE1 |= WDTIE;                               // Enable WDT interrupt



	// Enable interrupts
    __enable_interrupt();                       // Enable System interrupts

    InitTimerSystem();

    InitDriver();
    InitDisplay();
    BlankScreen();
    DisplayFrame();

    // Load an initial image of Sonic
    BlankScreen();
    DisplayFrame();

    unsigned char selector = 0;
    InitFrameBuffer();
    OutString("  e-Ink DRIVER MSP430", Font2);
    LoadText(0, 0, 1);
    InitFrameBuffer();
    OutString("   Demo by Sonikku", Font2);
    LoadText(0, 16, 1);
    InitFrameBuffer();
    OutString("  Button toggles image", Font2);
    LoadText(0, 32, 1);
    InitFrameBuffer();
    OutString("    ** IMAGE 1 **", Font2);
    LoadText(0, 64, 1);
    InitFrameBuffer();
    OutString("      Doraemon", Font2);
    LoadText(0, 80, 1);
    InitFrameBuffer();
    OutString("    ** IMAGE 2 **", Font2);
    LoadText(0, 112, 1);
    InitFrameBuffer();
    OutString("  Sonic The Hedgehog", Font2);
    LoadText(0, 128, 1);


    DisplayFrame();

//    Delay(5000);
//    LoadBitmap(Sonic);  // Buffer A
//    DisplayFrame();
//    LoadBitmap(Sonic); // Buffer B
//    DisplayFrame();


	for(;;){

	    // Poll for button
	    while((P1IN & S2) != 0){
	        ;
	    }

	    P1OUT |= LED1;

	    switch (selector){

	    case 0x00:
           LoadBitmap(Doraemon);
           DisplayFrame();
           LoadBitmap(Doraemon);
            DisplayFrame();
            selector = 1;
	        break;


	    case 0x01:
          LoadBitmap(Sonic);
          DisplayFrame();
          LoadBitmap(Sonic);
           DisplayFrame();
           selector = 0;

	        break;



	    }


	    P1OUT &= ~LED1;

	    // Wait for button release
        while((P1IN & S2) == 0){
            ;
        }

	}
}



